            <h1 class="font-bold text-2xl">Mr Easy’s International Hit ‘Drive Me Crazy’ Featured on BBC TV Series ‘Grime Kids’</h1>
  
            <div class="by-line">
              <span>By</span> <span class="author">Lee</span> -
              <span class="date opacity-50">January 7, 2024</span>
            </div>
  
            <img src="./public/images/post.png" alt="" class="post-image" />
  
            <div class="content">
              <div>
                The saying “Good music doesn’t have an expiration date” couldn’t
                ring more true for international recording artist Mr Easy, as his
                global Dancehall hit “Drive Me Crazy” is featured on&nbsp;<a
                  target="_blank"
                  rel="noopener noreferrer"
                  href="demon.link"
                  >“Grime Kids,”&nbsp;</a
                >a drama series now playing on BBC Three.
              </div>
              <div><br /></div>
              <div>
                “Drive Me Crazy,” Mr Easy’s Tony Kelly produced staple (<a
                  target="_blank"
                  rel="noopener noreferrer"
                  href="demon.link"
                  >Buy Out Riddim</a
                >), is raking in accolades amidst celebrating it’s 22nd
                Anniversary. The song, which one can hear on any given night
                around the globe, tops off it’s big year with a feature in “Grime
                Kids” — an acclaimed TV series, which charts the rise of a
                fictional group of young Black London teenagers coming of age in
                the late 90s, dreaming of being able to make their voice heard
                through grime music.
              </div>
              <div><br /></div>
              <div>
                “It is such an unbelievable, but gracious feeling to know that out
                of so many songs out there that ‘Drive Me Crazy’ was selected for
                the ‘Grime Kids’ series,” says Mr Easy. “Trust me, it means a lot
                that my classic Dancehall record is still getting love and music
                placements.”
              </div>
